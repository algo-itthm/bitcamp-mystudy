package study.lang.literal;

// 실습
// - 리터럴을 사용하여 다음과 같이 출력되게 하라.
// - 출력
//   100
//   3.14
//   가
//   가나다
//   true
// 
// 유형별 자료(Data Type)의 표현법(Literal)을 설명하라.
// - 기본 데이터 타입
// 1. 정수형: byte(1byte), short(2byte), int(4byte), long(8byte) / long은 숫자 뒤 L또는 l 을 붙임
// 2. 부동소수점형: float(4byte), double(8byte) / float은 숫자 뒤 F 또는 f 를 붙임
// 3. 문자형: char(2byte) / '' 사용
// 4. 논리형: boolean / true or false
// 
// - 참조 데이터 타입
// 
// 
// 
// 
// 
// 
// 
public class Test01 {
    public static void main(String[] args) {
        // 코드를 완성하라.
        System.out.println(100); // 정수 리터럴 100
        System.out.println(3.14); // 부동소수점 리터럴 3.14
        System.out.println('가'); // 문자 '가'
        System.out.println("가나다"); // 문자열 '가나다'
        System.out.println(true); // 논리 true
    }
}
